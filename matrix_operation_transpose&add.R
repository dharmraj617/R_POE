# Create a matrix
matrix1 <- matrix(c(1, 2, 3, 4), nrow = 2, ncol = 2)

# Transpose
transpose_matrix <- t(matrix1)

# Create another matrix
matrix2 <- matrix(c(5, 6, 7, 8), nrow = 2, ncol = 2)

# Addition
addition_result <- matrix1 + matrix2

# Print the results
print("Transpose:")
print(transpose_matrix)

print("Addition:")
print(addition_result)
